"""Sectors and industry map data with cleaning utilities.

This module defines `INDUSTRY_MAP` (raw mapping) and exports a cleaned
`INDUSTRY_MAP` and `SECTORS` list after validation.

The cleaning runs on import so other modules can simply `from sectors import SECTORS`.
"""
from typing import Dict, List, Iterable


def validate_and_clean_industry_map(ind_map: Dict) -> Dict[str, List[str]]:
    """Normalize industry map keys and symbol lists.

    Rules:
    - Sector names -> uppercase, stripped
    - Symbols -> uppercase, stripped
    - Remove duplicates while preserving first-occurrence order
    - Remove empty/None entries
    - Ensure each value is a list
    """
    cleaned = {}
    if not isinstance(ind_map, dict):
        return cleaned

    for raw_key, raw_val in ind_map.items():
        if raw_key is None:
            continue
        key = str(raw_key).strip().upper()
        if not key:
            continue

        if not isinstance(raw_val, (list, tuple, set)):
            # Skip non-iterable value
            continue

        seen = set()
        out_list = []
        for item in raw_val:
            if item is None:
                continue
            s = str(item).strip().upper()
            if not s:
                continue
            if s in seen:
                continue
            seen.add(s)
            out_list.append(s)

        cleaned[key] = out_list

    return cleaned


def validate_and_clean_sectors(sectors: Iterable) -> List[str]:
    """Normalize a sequence of sector names to a cleaned list.

    - Convert to uppercase trimmed strings
    - Remove duplicates and empty entries
    - If `INDUSTRY_MAP` exists in module scope, filter to sectors that exist in it
    """
    out = []
    if sectors is None:
        return out

    seen = set()
    for s in sectors:
        if s is None:
            continue
        name = str(s).strip().upper()
        if not name or name in seen:
            continue
        seen.add(name)
        out.append(name)

    # If INDUSTRY_MAP exists, keep only keys present
    if 'INDUSTRY_MAP' in globals() and isinstance(globals()['INDUSTRY_MAP'], dict):
        available = set(globals()['INDUSTRY_MAP'].keys())
        out = [s for s in out if s in available]

    return out


